char	ft_int_to_char(int nb)
{
	char	c;

	if (nb == -2147483648)
	{
		c = "-2147483648";
		return (c);
	}
	else
	{
		if (nb < 0)
		{
			write(1, "-", 1);
			nb = -nb;
		}
		if (nb >= 10)
			ft_putnbr(nb / 10);
		c = nb % 10 + '0';
		return (c);
	}
}
